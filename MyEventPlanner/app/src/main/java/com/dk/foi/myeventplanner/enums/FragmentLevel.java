package com.dk.foi.myeventplanner.enums;

/**
 * Enum for establishing level of fragment that's being started by FragmentStarter
 * INDEX = index level fragments (main screen),
 * LEVEL_ONE = level one fragments,
 * LEVEL_TWO = level two fragments
 */
public enum FragmentLevel {
    INDEX,
    LEVEL_ONE,
    LEVEL_TWO
}
